#!/usr/bin/env node

import { execFile } from "node:child_process";
import { writeFile } from "node:fs/promises";
import path from "node:path";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const outputPath = path.resolve(process.cwd(), "src/core/api/types.ts");

function resolveSupabaseArgs() {
  const baseArgs = ["gen", "types", "typescript", "--schema", "public"];

  if (process.env.SUPABASE_DB_URL) {
    return [...baseArgs, "--db-url", process.env.SUPABASE_DB_URL];
  }

  if (process.env.SUPABASE_PROJECT_ID) {
    return [...baseArgs, "--project-id", process.env.SUPABASE_PROJECT_ID];
  }

  return [...baseArgs, "--local"];
}

function resolveNpxCommand(args) {
  if (process.platform === "win32") {
    return {
      command: "cmd",
      args: ["/c", "npx", ...args],
    };
  }

  return {
    command: "npx",
    args,
  };
}

async function main() {
  const cliArgs = ["--yes", "supabase", ...resolveSupabaseArgs()];
  const command = resolveNpxCommand(cliArgs);

  const { stdout } = await execFileAsync(command.command, command.args, {
    maxBuffer: 20 * 1024 * 1024,
  });

  const banner = [
    "/* eslint-disable */",
    "// This file is auto-generated by `npm run db:types`.",
    "// Encoding is always UTF-8 to avoid parser issues on Windows.",
    "",
  ].join("\n");

  const content = `${banner}${stdout.trimEnd()}\n`;
  await writeFile(outputPath, content, "utf8");
  console.log(`Supabase types written (UTF-8): ${outputPath}`);
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`Failed to generate Supabase types: ${message}`);
  process.exitCode = 1;
});


